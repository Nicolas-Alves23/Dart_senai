// utilizamos o while quando não sabemos quantas vezes vai repetir
// Do While = faça enquanto
import 'dart:io';

void main(){
  int number = 0;
  
  do { // ele faz uma vez caso 
    print("hello"); // fez 
  } while (number > 1); // Após ele verifica se está correto 
  // ou seja acarretando na finalização da repetição 
}